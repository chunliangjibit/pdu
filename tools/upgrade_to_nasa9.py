def upgrade_products(json_path):
    with open(json_path, 'r') as f:
        db = json.load(f)
        
    for s, data in nasa9_data.items():
        if s in db['species']:
            db['species'][s].update(data)
            print(f"Upgraded {s} to NASA 9 coefficients.")
        else:
            print(f"Warning: {s} not found in database.")
            
    with open(json_path, 'w') as f:
        json.dump(db, f, indent=2)

